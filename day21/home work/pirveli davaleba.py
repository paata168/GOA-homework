#Slicing: ეს არის მეთოდი, რომელიც საშუალებას გვაძლევს ავიღოთ სიის ან სხვა კოლექციის ელემენტების ნაწილობრივი ნაწილი. slicing საშუალებას გვაძლევს გადავიტანოთ მინის მთელი დიაპაზონი, მაგალითად, ელემენტებიდან 2-დან 5-მდე.



# მაგალითი:
