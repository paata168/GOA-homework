#Indexing: ეს არის მეთოდი, როდესაც ჩვენ კონკრეტულ ელემენტს ვიღებთ სიის ან სხვა კოლექციიდან მისი ინდექსის მეშვეობით. ინდექსი განსაზღვრავს ელემენტის პოზიციას სიის მასივში. მაგალითად, ინდექსითაც შეიძლება მივიღოთ მხოლოდ ერთი ელემენტი (რიცხვი ან სიმბოლო).

