#მომხმარებელს შემოატანინეთ მისი ასაკი შემდეგ გაყავით 2ზე, დაუბეჭდეთ შედეგიც და ახალ ხაზზე დაბეჭდეთ ამ მონაცემის class


# მომხმარებლის ასაკი
age = int(input("შეიტანეთ თქვენი ასაკი: "))

# ასაკის გაყოფა 2-ზე
age_divided = age / 2

# შედეგის დაბეჭდვა
print("ასაკის გაყოფილი 2-ზე: ", age_divided)

# მონაცემის კლასის დაბეჭდვა
print("ამ მონაცემის class:", type(age_divided))
