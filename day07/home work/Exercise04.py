#კომენტარებით ახსენით რას აკეთებს str(), int(), და float() ფუნქციები:


#str() ფუნქცია ცვლადს აქცევს სტრინგად 
num = 123
num_str = str(num)  
print(type(num_str)) 

#int() ფუნქცია ცვლადს აქცევს ინტეჯერად
str_num = "45"
num = int(str_num)  
print(type(num))  

#float() ფუნქცია ცვლადს აქცევს ფლოატად
num_str = "3.14"
num_float = float(num_str)  
print(type(num_float))  
