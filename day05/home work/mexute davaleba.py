#5)მოხმარებელს შემოატანინეთ რიცხვები ანუ num,num1,num2...და ასე შემდეგ და საბოლოოდ გამოითვალეთ მაგ რიცხების საშუალო არითმეტიკული
#მაგალიტად: 
#2, 2, 2 = 2 + 2 + 2 = 6 / 3 = 2 ანუ რიცხვების ჯამი გავყოთ რაოდენობაზე(მინიმუმ 3 რიცხვის ცვლადი გქონდეთ შექმნილი
# მომხმარებლის რიცხვების მიღება
num = float(input("შეიყვანეთ პირველი რიცხვი: "))
num1 = float(input("შეიყვანეთ მეორე რიცხვი: "))
num2 = float(input("შეიყვანეთ მესამე რიცხვი: "))

# საშუალო არითმეტიკის გამოთვლა
average = (num + num1 + num2) / 3

# შედეგის დაბეჭდვა
print(f"რიცხვების საშუალო არითმეტიკული არის: {average}")
