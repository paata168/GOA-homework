#3)გააკეთეთ 5 5-ი მაგალითი %-ზე და floor division-ზე //
# მაგალითი 1: %
num1 = 10
num2 = 3
result1 = num1 % num2
print(f"{num1} % {num2} = {result1}")  # 10-ის მოდული 3-სთან

# მაგალითი 2: %
num1 = 15
num2 = 4
result2 = num1 % num2
print(f"{num1} % {num2} = {result2}")  # 15-ის მოდული 4-სთან

# მაგალითი 3: //
num1 = 20
num2 = 6
result3 = num1 // num2
print(f"{num1} // {num2} = {result3}")  # 20-დან დაყოფა 6-ით (floor division)

# მაგალითი 4: //
num1 = 45
num2 = 8
result4 = num1 // num2
print(f"{num1} // {num2} = {result4}")  # 45-დან floor division 8-ით

# მაგალითი 5: %
num1 = 9
num2 = 2
result5 = num1 % num2
print(f"{num1} % {num2} = {result5}")  # 9-ის მოდული 2-სთან
