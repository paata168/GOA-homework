
# 4 ცალი 'or' ოპერატორის მაგალითი
a = True or False        # a = True
b = False or False       # b = False
c = False or True        # c = True
d = True or True         # d = True

# 4 ცალი 'and' ოპერატორის მაგალითი
e = True and False       # e = False
f = False and False      # f = False
g = True and True        # g = True
h = False and True       # h = False

# პრინტით ამოტანილი ყველა ცვლადი
print(a, b, c, d, e, f, g, h)
